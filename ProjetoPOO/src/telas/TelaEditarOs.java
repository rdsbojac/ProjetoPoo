    public boolean validaOs(int id) {
        Connection conexao = null;
        
        String sql = "select * from tbOs where id=?";
        
        try {
            // Gera conexão e Statement
            conexao = new ModuloConexao().conector();
            PreparedStatement stmt = conexao.prepareStatement(sql);
            
            //CONSULTA BANCO DE DADOS E SUBSTITUI OS "?" PELO CONTEUDO SELECIONADO
            stmt.setString(1, String.valueOf(id));
            
            //EXECUTA AS INTRUÇÕES
            ResultSet rs = stmt.executeQuery();
            
            // verifica se o funcionario existe no banco de dados
            if (rs.next()){
                return true;
            }else{
                JOptionPane.showMessageDialog(null, "id inválido!");
                return false;
            }
        } catch (SQLException e) {
            throw new RuntimeException(e);
        }
    }
    
    public JComboBox pesquisaOs(JComboBox jComboBox) {
         
        try {
            // pega a conexão e o Statement
            Connection con = new ModuloConexao().conector();
            PreparedStatement stmt = con.prepareStatement("select * from tbOs");
            
            // executa um select
            ResultSet rs = stmt.executeQuery();
            
            // itera no ResultSet
            while (rs.next()) {
                // Recolhe os valores
                int id = rs.getInt("id");
                String veiculo = rs.getString("veiculo");
                String defeito = rs.getString("defeito");
                String servico = rs.getString("servico");
                
                // Junta os valores uma String
                String item = String.valueOf(id) + " " + veiculo + " " + defeito + " " + servico;
                
                // Salva no JComboBox
                jComboBox.addItem(item);
            }
            
            // Fecha tudo
            rs.close();
            stmt.close();
            con.close();
            
            return jComboBox;
        } catch (SQLException e) {
            throw new RuntimeException(e);
        }
    }

