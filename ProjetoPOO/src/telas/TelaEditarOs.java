    private void EditarOsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarOsButtonActionPerformed
        // TODO add your handling code here:
        // Recolhe todos os valores
        String valorServico = valorServicoInput.getText().strip();
        String veiculo = veiculoInput.getText().strip();
        String defeito = defeitoInput.getText().strip();
        String servico = servicoInput.getText().strip();
        int idMecanico = (int) idMecanicoInput.getValue();

        // Recolhe os valores da os selecionada
        int id = 0;
        String item = (String) listaOs.getSelectedItem();
        String[] listaItens = item.split(" ");

        // Verifica se todos os campos foram preenchidos

        boolean valido = false;
        if (valorServico.isBlank() || veiculo.isBlank() || defeito.isBlank() || servico.isBlank()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            // converte todo o id passado para inteiro-
            id = Integer.parseInt(listaItens[0]);

            // Verifica se a os indicado existe na tabela
            valido = validaOs(id);
            System.out.println("id: " + id + " é valido");
        }

        // Salva no banco de dados se for valido
        if (valido) {

            OrdemServico os = new OrdemServico();

            // Passa todas os valores de ordem de serviço
            os.setVeiculo(veiculo);
            os.setDefeito(defeito);
            os.setServico(servico);
            os.setMecanico(idMecanico);
            os.setValorServico(valorServico);
            os.setOs(id);

            // Salva no banco de dados
            UsuarioDao dao = new UsuarioDao();
            dao.editarOs(os);
        }

    }//GEN-LAST:event_EditarOsButtonActionPerformed

